interface AdminSidebarProps {
  unreadNotificationsCount?: number;
}

export default function AdminSidebar({
  unreadNotificationsCount = 0,
}: AdminSidebarProps) {
  const pathname = usePathname();
  const locale = useLocale();
  const [isMobileOpen, setIsMobileOpen] = useState(false);

  const buildHref = (href: string) => `/${locale}${href}`;

  const isActive = (href: string) => {
    const fullHref = buildHref(href);
    if (href === "/admin") {
      return pathname === fullHref;
    }
    return pathname?.startsWith(fullHref);
  };

  const sidebarContent = (
    <>
      {/* Logo / Brand */}
      <div className="p-6 border-b border-neutral-200">
        <Link href={buildHref("/")} className="flex items-center gap-3">
          <div className="w-8 h-8 bg-neutral-900 rounded-lg flex items-center justify-center">
            <span className="text-white text-sm font-bold">V</span>
          </div>
          <div>
            <h1 className="text-sm font-semibold text-neutral-900">Vento Caffe</h1>
            <p className="text-xs text-neutral-500">Admin Panel</p>
          </div>
        </Link>
      </div>

      {/* Navigation */}
      <nav className="flex-1 p-4 space-y-1">
        {navItems.map((item) => (
          <Link
            key={item.href}
            href={buildHref(item.href)}
            onClick={() => setIsMobileOpen(false)}
            className={`flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-colors ${
              isActive(item.href)
                ? "bg-neutral-900 text-white"
                : "text-neutral-600 hover:bg-neutral-100 hover:text-neutral-900"
            }`}
          >
            {item.icon}
            <span>{item.label}</span>
            {item.href === "/admin/notifications" && unreadNotificationsCount > 0 ? (
              <span
                className={`ml-auto inline-flex min-w-[1.5rem] items-center justify-center rounded-full px-1.5 py-0.5 text-xs ${
                  isActive(item.href)
                    ? "bg-white/20 text-white"
                    : "bg-neutral-200 text-neutral-700"
                }`}
              >
                {unreadNotificationsCount > 99 ? "99+" : unreadNotificationsCount}
              </span>
            ) : null}
          </Link>
        ))}
      </nav>

      {/* Back to store */}
      <div className="p-4 border-t border-neutral-200">
        <Link
          href={buildHref("/")}
          className="flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium text-neutral-600 hover:bg-neutral-100 hover:text-neutral-900 transition-colors"
        >
          <svg className="w-5 h-5" fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor">
            <path strokeLinecap="round" strokeLinejoin="round" d="M9 15 3 9m0 0 6-6M3 9h12a6 6 0 0 1 0 12h-3" />
          </svg>
          Back to Store
        </Link>
      </div>
    </>
  );

  return (
    <>
      {/* Mobile toggle */}
      <button
        onClick={() => setIsMobileOpen(!isMobileOpen)}
        className="lg:hidden fixed top-4 left-4 z-50 p-2 bg-white rounded-lg shadow-md border border-neutral-200"
        aria-label="Toggle menu"
      >
        <svg className="w-5 h-5" fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor">
          {isMobileOpen ? (
            <path strokeLinecap="round" strokeLinejoin="round" d="M6 18 18 6M6 6l12 12" />
          ) : (
            <path strokeLinecap="round" strokeLinejoin="round" d="M3.75 6.75h16.5M3.75 12h16.5m-16.5 5.25h16.5" />
          )}
        </svg>
      </button>

      {/* Mobile overlay */}
      {isMobileOpen && (
        <div
          className="lg:hidden fixed inset-0 bg-black/20 z-40"
          onClick={() => setIsMobileOpen(false)}
        />
      )}

      {/* Sidebar */}
      <aside
        className={`fixed top-0 left-0 h-full w-64 bg-white border-r border-neutral-200 flex flex-col z-40 transition-transform duration-200 lg:translate-x-0 ${
          isMobileOpen ? "translate-x-0" : "-translate-x-full"
        }`}
      >
        {sidebarContent}
      </aside>
    </>
  );
}
